public final void inicializaComboEstoque() throws Exception {
        DefaultComboBoxModel comboEstoque = new DefaultComboBoxModel();
        comboEstoque.addElement("Selecione Ingrediente do Estoque");
        
        //est = new Estoque();
        estDAO = new EstoqueDAO();
        ing = new Ingrediente();
        estoque = estDAO.listaTodoEstoque(); //ingDAO.listaTodosIngredientes(ing);
        
        for(Estoque e: estoque){
            ingDAO = new IngredienteDAO();
            //System.out.println(e.getIdIngrediente());
            ingredienteEstoque = ingDAO.consultaTodosIngredientes(e.getIdIngrediente());
            for(Ingrediente i: ingredienteEstoque){
                //System.out.println("aki");
                //System.out.println(i.getNomeIngrediente());
                comboEstoque.addElement("Nome " + i.getNomeIngrediente() + " Lote " + e.getNumeroLoteEstoque());
            }
        }
        
        cbIngredienteEstoque.setModel(comboEstoque);
        
        //cbIngrediente.setSelectedIndex(0);
    }
public final void inicializaComboIngrediente() throws Exception {
        DefaultComboBoxModel comboIngrediente = new DefaultComboBoxModel();
        comboIngrediente.addElement("Selecione Ingrediente");
        ingDAO = new IngredienteDAO();
        ing = new Ingrediente();
        ingredientes = ingDAO.listaTodosIngredientes(ing);
        for (Ingrediente i : ingredientes) {
            comboIngrediente.addElement("ID " + i.getIdIngrediente() + " " +i.getNomeIngrediente());
        }
        cbIngrediente.setModel(comboIngrediente);
        
        //cbIngrediente.setSelectedIndex(0);
    }
