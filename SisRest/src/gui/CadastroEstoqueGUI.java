    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(cbIngrediente.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null,"Selecione um Ingrediente para Cadastro !");
        }
       else{

        if(txtQntd.getText().equals("") || txtNumeroLote.getText().equals("") || txtDataValidade.getText().equals("  /  /    ")){
            JOptionPane.showMessageDialog(null,"Campos vazios!!");
        }else{
        ing = new Ingrediente();
        est = new Estoque();
        
        try {
            defineIdsIngrediente();
            //System.out.println("" + ing.getIdIngrediente());
            est.setQtdEstoque(Integer.parseInt(txtQntd.getText()));
            
            //Transformando String para sql.Date
            String dataString = txtDataValidade.getText();
            
            DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date dataValidadeTemp = new java.sql.Date(fmt.parse(dataString).getTime());
            
            est.setDataVencimentoEstoque(dataValidadeTemp);
            est.setNumeroLoteEstoque(txtNumeroLote.getText());
            
            estDAO = new EstoqueDAO();
           
            estDAO.cadastrar(est,ing.getIdIngrediente());
            JOptionPane.showMessageDialog(null,"Cadastrado com sucesso");
            //System.out.println("aki");
            inicializaComboEstoque();
            limpaCampos();
        } catch (Exception ex) {
            Logger.getLogger(CadastroEstoqueGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

