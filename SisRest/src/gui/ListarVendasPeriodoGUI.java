    private void tabelaVendasPeriodoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaVendasPeriodoMouseClicked
        limparProdTable();
        vend = new Venda();
        prod = new Produto();
        pV = new ProdutoHasVenda();
        int linha = tabelaVendasPeriodo.getSelectedRow();
        
//        ProdutoVendaTableModel modelo = (ProdutoVendaTableModel) tabelaProdutos.getModel();
        
        try {
            VendasPeriodoTableModel modelo = (VendasPeriodoTableModel)tabelaVendasPeriodo.getModel();
        
            vend.setIdVendas(Integer.parseInt(modelo.getValueAt(linha,0).toString()));
        
            pvDAO = new ProdutoDaVendaDAO();
            prodDAO = new ProdutoDAO();
            
            produtos = prodDAO.listaDadosProdPeloID(vend.getIdVendas());
            
            if(produtos.size() == 0){
                JOptionPane.showMessageDialog(null,"Nenhum produto nessa venda");
            }
            produtoVenda = pvDAO.listaQntdProduto(vend.getIdVendas());
            
            ProdutoVendaTableModel modelo2 = (ProdutoVendaTableModel) tabelaProdutos.getModel();
            
            for(int i=0 ; i < produtos.size() ; i++){
                
                prod.setIdProduto(produtos.get(i).getIdProduto());
                prod.setNomeProduto(produtos.get(i).getNomeProduto());
                prod.setPrecoProduto(produtos.get(i).getPrecoProduto());
                pV.setQtdPrdVenda(produtoVenda.get(i).getQtdPrdVenda());
                
                modelo2.addRow(new Produto(prod.getIdProduto(),prod.getNomeProduto(),prod.getPrecoProduto()), new ProdutoHasVenda(pV.getQtdPrdVenda()));
            }
            
            
        } catch (Exception ex) {
            Logger.getLogger(ListarVendasPeriodoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabelaVendasPeriodoMouseClicked

