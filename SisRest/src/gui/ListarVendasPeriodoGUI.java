    public void limparVendaTable() {
        VendasPeriodoTableModel modelo = (VendasPeriodoTableModel)tabelaVendasPeriodo.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
}
}
    public void limparProdTable() {
        ProdutoVendaTableModel modelo2 = (ProdutoVendaTableModel) tabelaProdutos.getModel();
        while (modelo2.getRowCount() > 0) {
            modelo2.removeRow(0);
}
}
    public void populaTable(){
        try {
            
            vendDAO = new VendaDAO();
            vend = new Venda();
            cli = new Cliente();
            VendasPeriodoTableModel modelo = (VendasPeriodoTableModel)tabelaVendasPeriodo.getModel();
            SimpleDateFormat formatdata = new SimpleDateFormat("dd/MM/yyyy"); 
    
            Date dataIniTemp = new Date (formatdata.parse(txtDataInicio.getText()).getTime());
            Date dataFimTemp = new Date (formatdata.parse(txtDataFim.getText()).getTime());
  
            vendas = vendDAO.listaVendaPeriodo(dataIniTemp,dataFimTemp);
            if(vendas.size() == 0){
                JOptionPane.showMessageDialog(null,"Nenhumas Venda com status pronto Realizada nesse Período!");
            }
            for(Venda v: vendas){
                cliDAO = new ClienteDAO();
                clientes = cliDAO.consultaClienteID(v.getIdCliente());
                
                vend.setIdVendas(v.getIdVendas());
                vend.setDataVendas(v.getDataVendas());
                
                for(Cliente c: clientes){
                    
                    cli.setNomeCliente(c.getNomeCliente());
                    
                    modelo.addRow(new Venda(vend.getIdVendas(),vend.getDataVendas()),new Cliente(cli.getNomeCliente()));
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(ListarVendasPeriodoGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
            
    }
    
