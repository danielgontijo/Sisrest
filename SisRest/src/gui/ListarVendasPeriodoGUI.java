    private double calculaTotalPeriodo() throws Exception{
        vend = new Venda();
        
        double tempTotal = 0;
                
        prod = new Produto();
        
        SimpleDateFormat formatdata = new SimpleDateFormat("dd/MM/yyyy");
        
        Date dataIniTemp = new Date (formatdata.parse(txtDataInicio.getText()).getTime());
        Date dataFimTemp = new Date (formatdata.parse(txtDataFim.getText()).getTime());
        
        vendDAO = new VendaDAO();
        vendasCalc = vendDAO.listaVendaPeriodo(dataIniTemp,dataFimTemp);
        
        
        for(Venda v: vendasCalc){
            
            prodDAO = new ProdutoDAO();
            produtos = prodDAO.listaDadosProdPeloID(v.getIdVendas());
            pvDAO = new ProdutoDaVendaDAO();
            produtoVenda = pvDAO.listaQntdProduto(v.getIdVendas());
            for(Produto p: produtos){
                
                for(ProdutoHasVenda pV: produtoVenda){
//                    System.out.println(p.getNomeProduto() + " " + p.getPrecoProduto());
//                    System.out.println(pV.getQtdPrdVenda());
                    tempTotal = tempTotal + p.getPrecoProduto() * pV.getQtdPrdVenda();
                }
            }
        }
//            prodDAO = new ProdutoDAO();
            
//            
          
        return tempTotal;
    }

