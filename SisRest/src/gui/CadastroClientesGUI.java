    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
       if("".equals(txtNome.getText()) || "".equals(txtSobreNome.getText()) || "".equals(txtCpf.getText())){
            JOptionPane.showMessageDialog(null,"Campos vazios !");
        }else{
        
        Cliente cli = new Cliente();
        try {
            ClienteDAO cliDAO = new ClienteDAO();
            cli.setNomeCliente(txtNome.getText());
            cli.setSobreNomeCliente(txtSobreNome.getText());
            cli.setCpfCliente(txtCpf.getText());
            //System.out.println("" + cli.getCpfCliente());
            cliDAO.salvar(cli);
        } catch (Exception ex) {
            Logger.getLogger(CadastroReceitaGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null,"Cliente Cadastrada com Sucesso");
        
        try {
            inicializaComboCliente();
            limpaCampos();
        } catch (Exception ex) {
            Logger.getLogger(CadastroReceitaGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
      
    }//GEN-LAST:event_btnCadastrarActionPerformed

