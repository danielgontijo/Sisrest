    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(cbClientes.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Selecione um Cliente!");
        }else{
            try {
                cli = new Cliente();
                selecionaIdsCliente();
                
                cli.setIdCliente(cli.getIdCliente());
                
                cliDAO = new ClienteDAO();
                cliDAO.pesquisar(cli);

                txtNome.setText(cli.getNomeCliente());
                txtSobreNome.setText(cli.getSobreNomeCliente());
                txtCpf.setText(cli.getCpfCliente());
            } catch (Exception ex) {
                Logger.getLogger(CadastroClientesGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

