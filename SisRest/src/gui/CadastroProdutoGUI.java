    public final void inicializaComboReceita() throws Exception {
        DefaultComboBoxModel comboReceita = new DefaultComboBoxModel();
        comboReceita.addElement("Selecione Receita");
        receDAO = new ReceitaDAO();
        rece = new Receita();
        receitas = receDAO.listaTodasReceitas(rece);
        for (Receita r : receitas) {
            comboReceita.addElement(r.getNomeReceita());
        }
        cbReceita.setModel(comboReceita);
        //cbReceita.setSelectedIndex(0);
    }
     public final void inicializaComboProduto() throws Exception {
        DefaultComboBoxModel comboProduto = new DefaultComboBoxModel();
        comboProduto.addElement("Selecione Produto");
        prodDAO = new ProdutoDAO();
        prod = new Produto();
        produtos = prodDAO.listaTodosProdutos(prod);
        for (Produto p : produtos) {
            comboProduto.addElement(p.getNomeProduto());
        }
        cbProduto.setModel(comboProduto);
        //decidir se vai setar 0 ou nao
        //cbProduto.setSelectedIndex(0);
    }
    private void selecionaIds(){
     int ind = cbReceita.getSelectedIndex();
        if (ind < 1) {
            rece = null;
            return;
        }
        ind--;
        rece = receitas.get(ind);
        
    }
     private void selecionaIdsProduto(){
     int ind = cbProduto.getSelectedIndex();
        if (ind < 1) {
            rece = null;
            return;
        }
        ind--;
        prod = produtos.get(ind);
        
    }
