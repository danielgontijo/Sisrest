    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
          if(cbProduto.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null,"Selecione um produto");
        }else{    
        selecionaIds();
        //idReceitaTemp = rece.getIdReceita();
        int idProdutoTemp = prod.getIdProduto();
            
        try {
             if("".equals(txtNomeProduto.getText()) || "".equals(txtPreco.getText())){
            JOptionPane.showMessageDialog(null,"Campo de receita vazio!");
             }else{
            rece = new Receita();
            prod = new Produto();
            
            prodDAO = new ProdutoDAO();
                
            //rece.setIdReceita(idReceitaTemp); 
            prod.setIdProduto(idProdutoTemp);
            prod.setNomeProduto(txtNomeProduto.getText());
            prod.setPrecoProduto(Double.parseDouble(txtPreco.getText()));
            
                // System.out.println("" + prod.getIdProduto());
            //rece.setNomeReceita(txtReceita.getText());
            //receDAO.atualizar(rece);
            prodDAO.atualizar(prod);
            
            JOptionPane.showMessageDialog(null,"Produto Alterado com sucesso!");
            inicializaComboProduto();
            }
             
        } catch (Exception ex) {
            Logger.getLogger(CadastroReceitaGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtNomeProduto.setText("");
        txtPreco.setText("");
         
      }
    }//GEN-LAST:event_btnAlterarActionPerformed

