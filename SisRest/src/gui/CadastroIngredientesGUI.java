    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
         if(cbIngrediente.getSelectedIndex() == 0){
             JOptionPane.showMessageDialog(null,"Selecione um Ingrediente!");
         }else{
         try {
             if("".equals(txtNomeIng.getText()) || "".equals(txtPrecoIng.getText())){
                    JOptionPane.showMessageDialog(null,"Campos Vazios");
                }else{
             ingredienteDAO = new IngredienteDAO();
             ingrediente = new Ingrediente();
             ingrediente.setIdIngrediente(cbIngrediente.getSelectedIndex());
             ingrediente.setNomeIngrediente(txtNomeIng.getText());
             ingrediente.setPrecoIngrediente(Double.parseDouble(txtPrecoIng.getText()));
             //System.out.println("" + ingrediente.getIdIngrediente());
             ingredienteDAO.atualizar(ingrediente);
             JOptionPane.showMessageDialog(null,"Ingrediente Alterado com sucesso!");
             inicializaComboIngrediente();
             limpaCampos();
                 }
         } catch (Exception ex) {
             Logger.getLogger(CadastroIngredientesGUI.class.getName()).log(Level.SEVERE, null, ex);
         }
       }
    }//GEN-LAST:event_btnAlterarActionPerformed

